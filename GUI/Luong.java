    private void tblluongMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblluongMouseClicked
        try {
            int row = this.tblluong.getSelectedRow(); // lấy dòng hiện tại mình đang nhấn chuột
            String IDrow = (this.tblluong.getModel().getValueAt(row, 0)).toString(); //lấy giá trị dòng i vừa nhấn
            //và đổi giá trị đó ta string
            String sql1 = "select * from dbo.LUONG where MALUONG = '"+ IDrow +"' ";
            ResultSet rs = UpdataTable.showTextfield(sql1);
            //đọc dữ liệu tại dòng
            if(rs.next()) // nếu có dữ liệu
            {
                //MaP = rs.getString("MAKH");
                this.txtml.setText(rs.getString("MALUONG"));
                this.txtthang.setText(rs.getString("THANG"));
                this.txtltu.setText(rs.getString("LUONGTAMUNG"));
                this.txtnlv.setText(rs.getString("NGAYLAMVIEC"));
                this.txttienluong.setText(rs.getString("TIENLUONG"));
                this.txtbhxh.setText(rs.getString("BHXH"));
                this.txtbhyt.setText(rs.getString("BHYT"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblluongMouseClicked

