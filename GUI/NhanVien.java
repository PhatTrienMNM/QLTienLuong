    private void tblnvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblnvMouseClicked
        try {
            int row = this.tblnv.getSelectedRow(); // lấy dòng hiện tại mình đang nhấn chuột
            String IDrow = (this.tblnv.getModel().getValueAt(row, 0)).toString(); //lấy giá trị dòng i vừa nhấn
            //và đổi giá trị đó ta string
            String sql1 = "select * from dbo.NHANVIEN where MANV = '"+ IDrow +"' ";
            ResultSet rs = UpdataTable.showTextfield(sql1);
            //đọc dữ liệu tại dòng
            if(rs.next()) // nếu có dữ liệu
            {
                //MaP = rs.getString("MAKH");
                this.txtmanv.setText(rs.getString("MANV"));
                this.txthtnv.setText(rs.getString("HOTENNV"));
                this.txtdc.setText(rs.getString("DIACHI"));
                this.txttt.setText(rs.getString("THUONGTRU"));
                this.txtsdt.setText(rs.getString("SODIENTHOAI"));
                this.txtngaysinh.setText(rs.getString("NGAYSINH"));
                this.txtnoisinh.setText(rs.getString("NOISINH"));
                this.txtcmnd.setText(rs.getString("CMND"));
                this.txtqq.setText(rs.getString("QUEQUAN"));
                this.txtdt.setText(rs.getString("DANTOC"));
                this.txtghichu.setText(rs.getString("GHICHU"));
                this.txtmk.setText(rs.getString("MATKHAU"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tblnvMouseClicked

